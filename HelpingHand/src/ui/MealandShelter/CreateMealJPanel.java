    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:

        String foodType = txtMealType.getText();
        if ("".equals(foodType)) {
            JOptionPane.showMessageDialog(null, "Enter the food type!");
            return;
        }
        try {
            double foodPrice = Double.parseDouble(txtMealPrice.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Meal price should be integer value!!");
            txtMealPrice.setText("");
            return;
        }

        Meal food = ecoSystem.getMealDirectory().addMealType();
        food.setMealType(txtMealType.getText());
        food.setPrice(Double.parseDouble(txtMealPrice.getText()));
        populateMealTable();
        resetFields();
        JOptionPane.showMessageDialog(null, "Meal is created successfully");


    }//GEN-LAST:event_btnCreateActionPerformed


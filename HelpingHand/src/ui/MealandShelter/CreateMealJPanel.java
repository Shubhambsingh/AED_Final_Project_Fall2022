    private void populateMealTable() {
        DefaultTableModel table = (DefaultTableModel) tblMeal.getModel();
        table.setRowCount(0);
        for (Meal food : ecoSystem.getMealDirectory().getMealList()) {            
            Object[] row = new Object[3];
            row[0] = food.getMealId();
            row[1] = food.getMealType();
            row[2] = food.getPrice();
            table.addRow(row);
        }  
    }


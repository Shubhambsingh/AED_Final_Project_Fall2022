    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jAvaMealTable1.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select the row to assign the Request", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            MealOrgWorkRequest foodOrgWorkRequest = (MealOrgWorkRequest) jAvaMealTable1.getValueAt(selectedRow, 6);
            foodOrgWorkRequest.setStatus("Pending");
            foodOrgWorkRequest.setReceiver(userAccount);

            populateTable();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        
        int selectedRow = jAvaMealTable1.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select the row to approve the Request", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            MealOrgWorkRequest foodOrgWorkRequest = (MealOrgWorkRequest) jAvaMealTable1.getValueAt(selectedRow, 6);
            if(foodOrgWorkRequest.getStatus().equals("Pending")){
            foodOrgWorkRequest.setStatus("Approved");
            foodOrgWorkRequest.setReceiver(userAccount);
            foodOrgWorkRequest.setResolveDate(new Date());
            populateTable();
            }
            else{
              JOptionPane.showMessageDialog(null, "Request needs to be in Pending state to be Approved", "Warning", JOptionPane.WARNING_MESSAGE);
  
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jAvaMealTable1.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select the row to delete the Request", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to delete?", "Warning", dialogButton);
            if (dialogResult == JOptionPane.YES_OPTION) {
                MealOrgWorkRequest foodOrgWorkRequest = (MealOrgWorkRequest) jAvaMealTable1.getValueAt(selectedRow, 6);
                ecoSystem.getWorkQueue().getWorkRequestList().remove(foodOrgWorkRequest);
                populateTable();
            }
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
      
        int selectedRow = jAvaMealTable1.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select the row to reject the Request", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            MealOrgWorkRequest foodOrgWorkRequest = (MealOrgWorkRequest) jAvaMealTable1.getValueAt(selectedRow, 6);
            if (foodOrgWorkRequest.getStatus().equals("Pending")) {
                foodOrgWorkRequest.setStatus("Rejected");
                foodOrgWorkRequest.setReceiver(userAccount);
                foodOrgWorkRequest.setResolveDate(new Date());
                populateTable();
            } else {
                JOptionPane.showMessageDialog(null, "Request needs to be in Pending state to be Rejected", "Warning", JOptionPane.WARNING_MESSAGE);
            }
        }


    }//GEN-LAST:event_jButton4ActionPerformed


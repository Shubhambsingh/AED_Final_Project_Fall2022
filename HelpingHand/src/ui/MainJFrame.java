    private void loginButtonMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_loginButtonMousePressed
        // TODO add your handling code here:
        String userName = jTxtUsername.getText();
        // Get Password
        char[] passwordCharArray = jPasswordField1.getPassword();
        String password = String.valueOf(passwordCharArray);
        
         //Step1: Check in the system user account directory if you have the user
         
        userAccount = ecoSystem.getUserDetailsDirectory().authenticateUser(userName, password);
         inEnterprise = null;
         inOrganization = null;
        
        if (userAccount == null) {
            //Step2: Go inside each network to check each enterprise
            for (Network network : ecoSystem.getNetworkList()) {
                //Step 2-a: Check against each enterprise
                for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                    userAccount = enterprise.getUserDetailsDirectory().authenticateUser(userName, password);
                    if (userAccount == null) {
                        //Step3: Check against each organization inside that enterprise
                        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
                            userAccount = organization.getUserDetailsDirectory().authenticateUser(userName, password);
                            
                            if (userAccount != null) {
                            //    System.out.println("organi"+ organization.getName());
                                inEnterprise = enterprise;
                                inOrganization = organization;
                                break;
                            }
                        }
                    } else {
                        inEnterprise = enterprise;
                        break;
                    }
                    if (inOrganization != null) {
                        break;
                    }
                }
                if (inEnterprise != null) {
                    break;
                }
            }
        }
       
        if (userAccount == null) {
            JOptionPane.showMessageDialog(null, "Invalid Credentails!");
            return;
        } else {
           
            jUserLabel.setText("Welcome " + userAccount.getEmployee().getName() + "!!");
            loginJPanel.setVisible(false);
            UserContainer.setVisible(true);
            leftPanel.setVisible(true);
            jLogoutLabel.setVisible(true);
            btnBackLabel.setVisible(false);
            jTxtUsername.setText("");
            jPasswordField1.setText("");
            changePanel1();
        }
//        loginBtn.setEnabled(false);
//        logoutJButton.setEnabled(true);
//        usernameTxt.setEnabled(false);
//        passwordTxt.setEnabled(false);
        
    }//GEN-LAST:event_loginButtonMousePressed

